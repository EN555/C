float add(float x, float y)		//fuction that calculate add numbers
{
	return x+y;
}
float sub(float x ,float y)		//function that sub numbers
{
	return x-y;
}
double mul(double x, int y)		//fuction that multiplication numbers
{
	return x*y;
}

double div(double x, int y)		//fuction that divide numbers
{
	return x/y;
}	                                   
